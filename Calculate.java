public class Calculate {
    public static void main(String[] args) {
        int n1 = 5;
        int n2 = 8;

        //Arithmetic Operators
        int addition = n1+n2; //13
        System.out.println(addition);

        int subtraction = n2-n1; //3
        System.out.println(subtraction);

        int multiplication = n1*n2; //40
        System.out.println(multiplication);

        int division = n2/n1; //1.6
        System.out.println(division);

        int remainder = n2%n1; //3
        System.out.println(remainder);



        //integer division - floor value
        // 24/5 = 4.8 -> floor value = 4
        // 31/3 = 10.33 -> floor value = 10

        //Modulus operator -> % (Remainder)
        // 8/5 -> quotient = 1 , remainder = 3
    }
}
