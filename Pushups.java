public class Pushups {
    public static void main(String[] args) {

        int s=3;
        int p =5;

        for(int j=1; j<=s;j++){
            System.out.println("Set"+j);
            for(int i=1 ; i<=p ; i++){
                System.out.println("Pushup"+i);
            }
        }
        
        
        
    }
}
