public class Assignment {
    public static void main(String[] args) {
        int a = 5; // 5 is assigned to variable a

        a += 3; // a = 5+3 = 8
        a -= 3; // a = 8-3 = 5
        a *= 5; // a = 5*5 = 25
        a /= 5; // a = 25/5 = 5
        a %= 3; // a = 5%3 = 2
        System.out.println(a); 
    }
}
