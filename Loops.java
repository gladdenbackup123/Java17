public class Loops {
    public static void main(String[] args) {
        //Types of loops - 1. for , 2. while , 3.do while
        for(int counter=1 ; counter<=5 ; counter++){
            System.out.println("Hello");
        }

        //counter=1 - hello
        //counter=2 - hello
        //counter=3 - hello
        //counter=4 - hello
        //counter=5 - hello
        //counter=6 -> condition not satisfied -> Loop terminated
    }
}
